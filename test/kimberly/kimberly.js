const calculateTotalPaymentHistoryAmount = (data) => {
  // Use reduce to iterate over the data and sum up the amounts inside paymentHistory
  const totalAmount = data.reduce((accumulator, item) => {
    // Check if paymentHistory exists in the current item and has an amount property
    if (item.paymentHistory && item.paymentHistory.amount) {
      // Add the amount to the accumulator
      return accumulator + item.paymentHistory.amount;
    }
    // If paymentHistory or amount is missing, return accumulator unchanged
    return accumulator;
  }, 0); // Initialize accumulator with 0

  return totalAmount;
};

